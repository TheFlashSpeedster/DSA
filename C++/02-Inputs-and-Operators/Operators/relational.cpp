#include<iostream>
using namespace std;

int main(){
  // == (Is Equals To)
  cout<<(4==3); // false = 0
  
  // != (Is Not Equals To)
  cout<<(4!=3); // true = 1
  
  // < (Is Less Than)
  cout<<(4<3);  // false = 0
  
  // > (Is Greater Than)
  cout<<(4>3);  // true = 1
  
  // <= (Is Less Than or Equals To)
  cout<<(4<=3); // false = 0
  
  // >= (Is Greater Than or Equals To)
  cout<<(4>=3); // true = 1

  return 0;
}
