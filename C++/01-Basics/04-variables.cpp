#include<iostream>
using namespace std;

int main(){
  int x = 5; // x is 5
  cout<<x<<endl;
  
  x = 7; // update x to 7
  cout<<x<<endl;
  
  x = x + 6; // +10 in x
  cout<<x<<endl;
  
  x = x - 20; // -20 in x
  cout<<x<<endl;

  int y = 10; // y is 10
  cout<<y<<endl;

  y += 5; // +5 in y
  cout<<y<<endl;

  return 0;
}