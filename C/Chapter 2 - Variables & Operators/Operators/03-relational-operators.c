#include <stdio.h>
int main(){

  // Equals (=) | Not Equals (!=)
  printf("%d\n", 5==10); // false (0)
  printf("%d\n", 5==5); // true (1)

  // Greater Than (>) | Greater Than or Equal To (>=)
  printf("%d\n", 5>10); // false (0)
  printf("%d\n", 5>=10); // false (0)

  // Less Than (<) | Less Than or Equal To (<=)
  printf("%d\n", 5<10); // true (1)
  printf("%d\n", 5<=10); // true (1)
}