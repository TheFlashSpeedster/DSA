// Printing a variable's value after compilation

#include <stdio.h>

int main() {
    int age = 22;
    printf("Age is %d\n", age);

    float pi = 3.14;
    printf("Pi is %f\n", pi);

    char star = '*';
    printf("Star is %c\n", star);
    return 0;
}

/*
\n - next line
%d - integer (int)
%f - floating (float)
%c - character (char)
*/